class Aluno{
    
    // ATRIBUTOS
    private String ra;
    private String nome;
    private String curso;

    // GETTERS
    public String getRa(){
        return ra;
    }

    public String getNome(){
        return nome;
    }

    public String getCurso(){
        return curso;
    }


    // SETTERS
    public void setRa(String ra){
        this.ra = ra;
    }

    public void setNome(String nome){
        this.nome = nome;
    }

    public void setCurso(String curso){
        this.curso = curso;
    }
}